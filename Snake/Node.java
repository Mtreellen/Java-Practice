public class Node {
    private int nodeX;
    private int nodeY;

    public Node(int nodeX, int nodeY) {

        this.nodeX = nodeX;
        this.nodeY = nodeY;
    }

    public int getNodeX() {

        return nodeX;
    }
    public void setNodeX(int nodeX) {

        this.nodeX = nodeX;
    }
    public int getNodeY() {

        return nodeY;
    }
    public void setNodeY(int nodeY) {

        this.nodeY = nodeY;
    }
}

